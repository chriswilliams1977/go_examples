package arithmetic

func Addition(x int, y int) int {
	return x + y
}

func Multiply(x int, y int) int {
	return x * y
}

func Divide(x int, y int) int {
	return x / y
}

func GreaterThan(x int, y int) bool {
	return x > y
}

func LessThan(x int, y int) bool {
	return x < y
}

func EqualTo(x int, y int) bool {
	return x == y
}

func NotEqualTo(x int, y int) bool {
	return x != y
}